function generateBoldAsciiArt($message): void
{
    $asciiMap = asciiMap();
    $ascii_rows = ["", "", "", "", ""];

    // Loop through each character in the message
    foreach (str_split(strtoupper($message)) as $char) {
        if (isset($asciiMap[$char])) {
            for ($i = 0; $i < count($ascii_rows); $i++) {
                $ascii_rows[$i] .= $asciiMap[$char][$i]. " "; // Add spacing between letters
            }
        }
    }

    // Print each row of the ASCII art
    foreach ($ascii_rows as $row) {
        echo $row . "\n";
    }
}

// Call the function with any message
$input_message = readline('Text : ');
generateBoldAsciiArt($input_message);